/// Returns list of models for a brand.
/// Falls back to ['Base Model'] for brands not in kCarModels.
List<String> getModels(String brand) =>
    kCarModels[brand]?.keys.toList() ?? ['Base Model'];

/// Returns list of trims for a brand + model combination.
/// Falls back to generic trims if not found.
List<String> getTrims(String brand, String model) =>
    kCarModels[brand]?[model] ?? ['Standard', 'Sport', 'Premium', 'Luxury'];
